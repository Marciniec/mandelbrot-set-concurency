public class MandelbrotCoordinates {
    private int x;
    private int y;
    private int iterations;

    public MandelbrotCoordinates(int x, int y, int iterations) {
        this.x = x;
        this.y = y;
        this.iterations = iterations;
    }

    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }

    public int getIterations() {
        return iterations;
    }

}
